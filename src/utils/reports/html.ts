function escapeHtml(value: string) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function imageSrc(value?: string | null) {
  if (!value) return "";
  if (value.startsWith("data:") || value.startsWith("http")) return value;
  return "";
}

