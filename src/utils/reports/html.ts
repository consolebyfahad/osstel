function reportShell(title: string, metaLines: string[], body: string) {
  return `<!DOCTYPE html>
<html>
  <head>
    <meta charset="utf-8" />
    <style>${BASE_STYLES}</style>
  </head>
  <body>
    <div class="header">
      <div class="brand">OSSTEL</div>
      <div class="title">${escapeHtml(title)}</div>
      <div class="meta">${metaLines.map((line) => escapeHtml(line)).join("<br />")}</div>
    </div>
    ${body}
    <div class="footer">Generated by OSSTEL · Hostel Management Platform</div>
  </body>
</html>`;
}

function summaryCards(summary: {
  expected: number;
  collected: number;
  pending: number;
  review: number;
}) {
  return `
    <div class="summary-grid">
      <div class="summary-card"><div class="summary-label">Expected</div><div class="summary-value">${formatCurrency(summary.expected)}</div></div>
      <div class="summary-card"><div class="summary-label">Collected</div><div class="summary-value">${formatCurrency(summary.collected)}</div></div>
      <div class="summary-card"><div class="summary-label">Pending</div><div class="summary-value">${formatCurrency(summary.pending)}</div></div>
      <div class="summary-card"><div class="summary-label">Under Review</div><div class="summary-value">${formatCurrency(summary.review)}</div></div>
    </div>
  `;
}

function statusBadge(status: string) {
  const label = rentStatusLabel(status);
  const cls =
    status === "paid"
      ? "badge-paid"
      : status === "review"
        ? "badge-review"
        : "badge-pending";
  return `<span class="badge ${cls}">${escapeHtml(label)}</span>`;
}

export function buildRentCollectionHtml(data: RentCollectionReportData) {
  const sectionsHtml = data.sections
    .map((section) => {
      const rows =
        section.records.length > 0
          ? section.records
              .map(
                (record) => `
              <tr>
                <td>${escapeHtml(record.resident.name)}</td>
                <td>${escapeHtml(record.resident.phone)}</td>
                <td>${escapeHtml(record.room.roomNumber)}</td>
                <td>${formatCurrency(record.amount)}</td>
                <td>${statusBadge(record.status)}</td>
                <td>${record.isOverdue ? "Yes" : "No"}</td>
              </tr>
            `,
              )
              .join("")
          : `<tr><td colspan="6">No rent records for this period.</td></tr>`;

      return `
        <div class="section-title">${escapeHtml(section.hostelName)}</div>
        ${summaryCards(section.summary)}
        <table>
          <thead>
            <tr>
              <th>Resident</th>
              <th>Phone</th>
              <th>Room</th>
              <th>Amount</th>
              <th>Status</th>
              <th>Overdue</th>
            </tr>
          </thead>
          <tbody>${rows}</tbody>
        </table>
      `;
    })
    .join("");

  const body = `
    ${summaryCards(data.totals)}
    ${sectionsHtml}
  `;

  return reportShell("Monthly Rent Collection Report", [
    `Period: ${formatMonthYear(data.month, data.year)}`,
    `Scope: ${data.scopeLabel}`,
    `Generated: ${formatReportDate(data.generatedAt)}`,
    `Prepared by: ${data.generatedBy}`,
  ], body);
}

